#include "ToonScene.h"
#include <QMouseEvent>

///Idea from http://www.opengl-tutorial.org/beginners-tutorials/tutorial-6-keyboard-and-mouse/

//----------------------------------------------------------------------------------------------------------------------
void ToonScene::mouseMoveEvent( QMouseEvent* _event )
{
  // note the method buttons() is the button state when event was called
  // that is different from button() which is used to check which button was
  // pressed when the mousePress/Release event is generated
  if ( m_win.rotate && _event->buttons() == Qt::LeftButton )
  {
    float diffx = _event->x() - m_win.origX;
    float diffy = _event->y() - m_win.origY;
    camera.horizontalAngle -= ( 0.01f * diffx );
    camera.verticalAngle += ( 0.01f * diffy );
    m_win.origX = _event->x();
    m_win.origY = _event->y();
    update();
  }
  // right mouse translate code
  else if ( m_win.translate && _event->buttons() == Qt::RightButton )
  {
    int diffX      = static_cast<int>( _event->x() - m_win.origXPos );
    int diffY      = static_cast<int>( _event->y() - m_win.origYPos );
    m_win.origXPos = _event->x();
    m_win.origYPos = _event->y();
    look += 0.01f * diffX * camera.right; //move the camera horizontally while keeping direction
    look += 0.01f * diffY * up; //..vertically to the "up"
    update();

  }
}


//----------------------------------------------------------------------------------------------------------------------
void ToonScene::mousePressEvent( QMouseEvent* _event )
{
  // that method is called when the mouse button is pressed in this case we
  // store the value where the maouse was clicked (x,y) and set the Rotate flag to true
  if ( _event->button() == Qt::LeftButton )
  {
    m_win.origX  = _event->x();
    m_win.origY  = _event->y();
    m_win.rotate = true;
  }
  // right mouse translate mode
  else if ( _event->button() == Qt::RightButton )
  {
    m_win.origXPos  = _event->x();
    m_win.origYPos  = _event->y();
    m_win.translate = true;
  }
}

//----------------------------------------------------------------------------------------------------------------------
void ToonScene::mouseReleaseEvent( QMouseEvent* _event )
{
  // that event is called when the mouse button is released
  // we then set Rotate to false
  if ( _event->button() == Qt::LeftButton )
  {
    m_win.rotate = false;
  }
  // right mouse translate mode
  if ( _event->button() == Qt::RightButton )
  {
    m_win.translate = false;
  }
}

//----------------------------------------------------------------------------------------------------------------------
void ToonScene::wheelEvent( QWheelEvent* _event )
{

  // check the diff of the wheel position (0 means no change)
  if ( _event->delta() > 0 )
  {
    camera.distance += 0.4f;
  }
  else if ( _event->delta() < 0 )
  {
    camera.distance -= 0.4f;
  }
  update();
}
